package string_methods_precticeAssignments;

public class IndexOf_Method {
    public static void main(String[]args){
        String str = "Beautiful";
        int result;
        result = str.indexOf('e');
        System.out.println(result);

        result = str.indexOf('t');
        System.out.println(result);

        result = str.indexOf('t');
        System.out.println(result);

        result = str.lastIndexOf('e');
        System.out.println(result);

        result = str.lastIndexOf('l');
        System.out.println(result);

        result = str.lastIndexOf('f');
        System.out.println(result);
    }
}
