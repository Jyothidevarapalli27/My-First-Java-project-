package com.perscholas.java.basics;

public class Post_Fix_Increment {
    public static void main (String [] args){
        int num = 5;

        System.out.println("before postfixIncrement: " + num);
        System.out.println("value return by the expression (postfixIncrement): " + num++);
        System.out.println("after postfixIncrement: " + num);


    }
}
